fun main(args: Array<String>) {
    println("Oi, \t\t\ttudo bem?")
    println("Oi, \rtudo bem?")
    println("Oi,\btudo bem?")
    print("Oi,\\tudo bem?\n")
    println("Oi,\"tudo\" bem?")

    /*
     * Enunciado: Crie um programa que mostre na tela
     * uma mensagem utilizando quebra de linha e aspas.
     * fim de tudo
     * olha só
     */
    print("Kotlin é\n\"SUPER\"\nFácil!")
    // Lembrando que \"significa "MOSTRE ASPAS NA TELA"


}
